clear variables

% Tutorial: Creates a simple mat file
% - input for the "main" function"

% Define station name
Station = 'Teststation';
% Define data
myData  = {...
    '20-Dec-2013 12:00:00', seconds(days(0)), 0.0, 0.0, 0.0; ...
    '21-Dec-2013 12:00:00', seconds(days(1)), 0.1, 0.1, 0.1; ...
    '22-Dec-2013 12:00:00', seconds(days(2)), 0.2, 0.2, 0.3; ...
    '23-Dec-2013 12:00:00', seconds(days(3)), 0.1, 0.1, 0.1; ...
    '24-Dec-2013 12:00:00', seconds(days(4)), 0.2, 0.1, 0.1; ...
    '25-Dec-2013 12:00:00', seconds(days(5)), 0.0, 0.3, 0.1; ...
    '26-Dec-2013 12:00:00', seconds(days(6)), 0.0, 0.2, 0.2; ...
    '27-Dec-2013 12:00:00', seconds(days(7)), 0.1, 0.1, 0.1; ...
    '28-Dec-2013 12:00:00', seconds(days(8)), 0.1, 0.1, 0.1 ...
    };

% Convert to table
Data    = cell2table( myData, ...
    'VariableNames', { 'date' 't' 'coord1' 'coord2' 'coord3' } );
% Convert date column to DATETIME, UTC timezone
Data.date = datetime( Data.date, 'InputFormat', 'dd-MMM-yyyy HH:mm:ss', ...
    'TimeZone', 'UTC' );

% Assign to currStation struct
currStation.Station = Station;
currStation.Data    = Data;

% Save it
save( 'Teststation.mat', 'currStation' )